        sendingDate: json['sendingDate'],
        senderName: json['senderName'],
        senderId: json['senderId'],
        receiverId: json['receiverId'],
        isSynched: json['isSynched  '],
        readOrNot: json['readOrNot '],
        softDeleteState: json['softDeleteState  '],
      );
}
