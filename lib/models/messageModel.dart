class MessageModel {
  int id,
      cpId,
      typeId,
      gradeId,
      classId,
      branchId,
      yearId,
      schoolId,
      termId,
      senderId,
      receiverId,
      readOrNot,
      isSynched,
      softDeleteState,
      noOfMsgs,
      noOfUnRead;

  String guid, title, content, senderName, sendingDate, lastContent;

  MessageModel(
      {this.id,
      this.cpId,
      this.guid,
      this.gradeId,
      this.classId,
      this.branchId,
      this.yearId,
      this.schoolId,
      this.termId,
      this.typeId,
      this.title,
      this.content,
      this.sendingDate,
      this.senderName,
      this.senderId,
      this.receiverId,
      this.isSynched,
      this.readOrNot,
      this.softDeleteState});
